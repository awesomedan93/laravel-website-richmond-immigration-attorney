            <h6 class="bold_text">WHAT IS A MASTER CALENDAR HEARING?</h6>
            <p>
                The Master Calendar Hearing is the first hearing in a deportation case held before an immigration judge. This short hearing marks the start of the efforts to have a person removed from the United States through deportation. This is the point at which you will either concede or fight the charges listed in your Notice to Appear. After that, you will have the chance to file for relief or explain to the court why you should be allowed to remain in the United States. You will have a chance to present your case at the Merits Hearing.
                ​</p>
            <h6 class="bold_text">WHAT IS A MERITS HEARING?</h6>
            <p>
                After your Master Calendar Hearing you will be given time to build your case to present at the Merits Hearing. The Merits Hearing is the last hearing before the immigration judge and at that time you can present final evidence and testimony on why you should be permitted to stay in the United States. The immigration judge will make a final decision regarding your deportation status. The Merits Hearing is also referred to as the Individual Hearing.
                ​</p>
            <h6 class="bold_text">CAN I APPEAL MY DEPORTATION CASE?</h6>
            <p>
                If you disagree with the final decision of the immigration judge at your Merits Hearing, you will have a chance to appeal within 30 days to the Board of Immigration Appeals.
